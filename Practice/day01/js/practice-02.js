
// Kullanıcıdan dikdörtgenin uzunluğunu ve genişliğini alalım
let dikdortgenUzunlugu = parseInt(prompt("Dikdörtgenin uzunluğunu girin:"));
let dikdortgenGenisligi = parseInt(prompt("Dikdörtgenin genişliğini girin:"));
 // Alanı hesaplayalım ve sonucu bir değişkene atayalım.
let dikdortgenAlani = dikdortgenUzunlugu * dikdortgenGenisligi;
 // Sonucu ekrana yazdıralım.
console.log("Dikdörtgenin alanı: " + dikdortgenAlani);

console.log(`Dikdörtgenin alanı: ${dikdortgenAlani}`);